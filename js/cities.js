// Функция для поиска городов по запросу
function searchCities(query) {
  if (!query || query.trim().length === 0) {
    return [];
  }
  
  const searchTerm = query.toLowerCase().trim();
  
  // Сначала города, начинающиеся с запроса
  const startsWithMatches = russianCities.filter(city => 
    city.name.toLowerCase().startsWith(searchTerm)
  );
  
  // Затем города, содержащие запрос
  const containsMatches = russianCities.filter(city => 
    city.name.toLowerCase().includes(searchTerm) && 
    !city.name.toLowerCase().startsWith(searchTerm)
  );
  
  return [...startsWithMatches, ...containsMatches].slice(0, 10); // Ограничиваем 10 результатами
}

